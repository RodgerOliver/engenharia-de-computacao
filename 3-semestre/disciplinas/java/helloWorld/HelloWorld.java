/*
 * ===== Linguagem Orientada a Objeto I =====
 *
 * Objeto é qualquer coisa que pode ser modelada por um software
 * Todo objeto possui características (atributos) e ações (métodos)
 * Objetos são instâncias de Classes
 * Classes são abstratas (template), Objetos são concretos
 *
 */

public class HelloWorld {
	/* static para executar o método sem a necessidade de instanciar */
	public static void main() {
		System.out.println("Hello World!");
	}
}

/*
 * $ # run in bash
 * $ javac HelloWorld.java
 * $ java HelloWorld
 *
 * $ # or run this one
 * $ javac Main.java -d classes && java -cp class/ Main
 */
